class UserRepository extends ServiceEntityRepository implements UserLoaderInterface
{
    public function __construct(RegistryInterface $registry)
    {
        parent::__construct($registry, User::class);
    }


    /*
        Este método lo hemos creado para poder traernos de la BD el usuario tanto por username como por email.
        Esto nos asegurará poder comprobar que el usuario se pueda loguear tanto por username como por email (y la correspondiente contraseña, claro está).

        El nombre del método 'loadUserByUsername' es obligatorio por implementar UserLoaderInterface
    */
    public function loadUserByUsername($username)
    {
        return $this->createQueryBuilder('u')
            ->where('u.username = :username OR u.email = :email')
            ->setParameter('username', $username)
            ->setParameter('email', $username)
            ->getQuery()
            ->getOneOrNullResult();
    }

